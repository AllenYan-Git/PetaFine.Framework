        /// 判断省份(位置)下是否存在此城市
        /// </summary>
        /// <param name="provincePosition">省份位置</param>
        /// <param name="city">城市名称</param>
        /// <returns>-1:位置超出限制; 1:存在; 0:不存在</returns>
        public static int ExistsCity(int provincePosition, string city)
        {
            //1. 判断省份位置是否有效
            if (provincePosition < 0 || provincePosition > 33)
            {
                return -1;
            }
            return arrCity[provincePosition].Contains(city) ? 1 : 0;
        } 
