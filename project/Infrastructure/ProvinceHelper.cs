        #region 根据省名称获取省的位置 + static int GetProvincePosition(string province)
        /// <summary>
        /// 根据省名称获取省的位置
        /// </summary>
        /// <param name="province">省份名称</param>
        /// <returns>-1:不存在</returns>
        public static int GetProvincePosition(string province)
        {
            for (int i = 0; i < arrProvince.Length; i++)
            {
                if (arrProvince[i] == province)
                {
                    return i;
                }
            }
            return -1;
        }
        #endregion

